<p align="left"><b>About Power Within Disability</b></p>
<p> In India there are about 2.68 crore Persons with Disability (PwDs) yet there meaningful skill training and employment largely remains unmet. Thus there is a need to connect the words of disability and Technology.<br> 
<br>We are a technology-enabled raining organization. We provide on a blended learning format on skills like Basic computing ,Teaching , Mass communication ,Tailoring, Sign Language course or a course on Braille  to PwDs  enabling them to become self-employed or employable.
<br>
<br>Our website P.W.D (Power Within Disability) would symbolize the power of the disabled by giving them an opportunity to acquire desired skills and further help them to stand together with the common man and outshine in various means.
We use a blended learning model to achieve these objectives. Eventually, evaluation, certification and employment support is provided to the successful learners.

We then follow it up by providing the candidate with Employment Opportunities that can be made available to him/her on the basis of role as well as location. The candidate will also be notified about the vacancies available according to the course completed by him.

